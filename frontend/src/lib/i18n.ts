export const LANGUAGE_OPTIONS: Array<{ value: Language; label: string }> = [
  { value: 'kk', label: 'Қазақша' },
  { value: 'ru', label: 'Русский' },
  { value: 'en', label: 'English' },
];

const localeMap: Record<Language, string> = {
  kk: 'kk-KZ',
  ru: 'ru-RU',
  en: 'en-US',
};

let activeLanguage: Language = 'kk';

const resolveEntry = (dictionary: TranslationDictionary, path: string): string | undefined => {
  const segments = path.split('.');
  let current: TranslationValue | undefined = dictionary;

  for (const segment of segments) {
    if (!current || typeof current === 'string') {
      return undefined;
    }

    current = current[segment];
  }

  return typeof current === 'string' ? current : undefined;
};

const interpolate = (template: string, params?: TranslationParams) => {
  if (!params) {
    return template;
  }

  return Object.entries(params).reduce(
    (result, [key, value]) => result.split(`{${key}}`).join(String(value)),
    template,
  );
};

export const setI18nLanguage = (language: Language) => {
  activeLanguage = language;
};

export const getI18nLanguage = () => activeLanguage;

export const getLocaleForLanguage = (language: Language = activeLanguage) => localeMap[language];

export const translate = (key: string, params?: TranslationParams, language: Language = activeLanguage) => {
  const localized = resolveEntry(dictionaries[language], key) ?? resolveEntry(dictionaries.kk, key);
  return interpolate(localized ?? key, params);
};

export const hasTranslation = (key: string, language: Language = activeLanguage) =>
  Boolean(resolveEntry(dictionaries[language], key) ?? resolveEntry(dictionaries.kk, key));
